// Cache dos textos PT originais (preenchida na primeira mudança para EN)
let ptCache = null;

// Guarda os valores PT atuais de todos os seletores dos dicionários
function buildPtCache() {
  ptCache = { texts: {}, html: {}, attrs: {} };
  for (const sel of Object.keys(EN_TEXTS)) {
    ptCache.texts[sel] = [...document.querySelectorAll(sel)].map(e => e.textContent);
  }
  for (const sel of Object.keys(EN_HTML)) {
    ptCache.html[sel] = [...document.querySelectorAll(sel)].map(e => e.innerHTML);
  }
  for (const [sel, attrs] of Object.entries(EN_ATTRS)) {
    ptCache.attrs[sel] = {};
    const el = document.querySelector(sel);
    if (el) for (const a of Object.keys(attrs)) ptCache.attrs[sel][a] = el.getAttribute(a);
  }
}

// Aplica um dicionário (string ou array de strings) aos elementos do seletor
function applyDict(dict, prop) {
  for (const [sel, valor] of Object.entries(dict)) {
    const els = [...document.querySelectorAll(sel)];
    els.forEach((el, i) => {
      const v = Array.isArray(valor) ? valor[i] : (i === 0 ? valor : undefined);
      if (v !== undefined && v !== null) el[prop] = v;
    });
  }
}

// Muda o idioma de toda a página (textos, atributos e dados da API)
function applyLang(lang) {
  if (lang === 'en' && !ptCache) buildPtCache();
  currentLang = lang;
  localStorage.setItem('lang', lang);

  const texts = lang === 'en' ? EN_TEXTS : (ptCache ? ptCache.texts : {});
  const html  = lang === 'en' ? EN_HTML  : (ptCache ? ptCache.html  : {});
  const attrs = lang === 'en' ? EN_ATTRS : (ptCache ? ptCache.attrs : {});
  applyDict(texts, 'textContent');
  applyDict(html, 'innerHTML');
  for (const [sel, mapa] of Object.entries(attrs)) {
    const el = document.querySelector(sel);
    if (el) for (const [a, v] of Object.entries(mapa)) el.setAttribute(a, v);
  }

  // Metadados e controlo do idioma
  document.documentElement.lang = lang === 'en' ? 'en' : 'pt';
  document.title = lang === 'en' ? 'Visit Guimarães – Tourism Portal' : 'Visitar Guimarães – Portal de Turismo';
  const toggle = document.getElementById('langToggle');
  if (toggle) {
    toggle.textContent = lang === 'en' ? 'EN | PT' : 'PT | EN';
    toggle.title = lang === 'en' ? 'Mudar idioma para Português' : 'Switch language to English';
  }

  // Botão "ver todos", cartões dinâmicos e eventos no novo idioma
  const showAllBtn = document.getElementById('showAllPlaces');
  if (showAllBtn) showAllBtn.textContent = showAllPOI ? L('showLess') : L('showAll');
  fetchPlaces(document.getElementById('tourismQuery')?.value.trim() || '');
  fetchEvents();
  if (typeof renderDefaultSuggestions === 'function') renderDefaultSuggestions();
}


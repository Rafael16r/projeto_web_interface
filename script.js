// Procura eventos no dia escolhido (campo "Quando?"), destaca os cartões
// correspondentes e escreve o resultado por baixo da lista de eventos
function destacaEventosNaData(dataISO) {
  const [, mes, dia] = dataISO.split('-').map(Number);
  const status = document.getElementById('eventsStatus');
  const encontrados = [];
  document.querySelectorAll('.event-card').forEach(card => {
    card.classList.remove('event-highlight');
    const d = parseInt(card.querySelector('.event-day')?.textContent, 10);
    const m = MESES_EVENTO[card.querySelector('.event-month')?.textContent.trim().toLowerCase()] || 0;
    if (d === dia && m === mes) {
      card.classList.add('event-highlight');
      encontrados.push(card.querySelector('h4')?.textContent);
    }
  });
  if (status) {
    status.textContent = encontrados.length
      ? `📅 ${L('eventFound')} ${String(dia).padStart(2, '0')}/${String(mes).padStart(2, '0')}: ${encontrados.join(' · ')}`
      : L('eventNone');
  }
  return encontrados.length > 0;
}

// Pesquisa nas ligações externas: destaca as que correspondem ao termo
// (insensível a acentos) e esbate as restantes; mostra a contagem
function filtraLigacoes(query) {
  const norm = (s) => (s || '').toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '');
  const cards = [...document.querySelectorAll('.ligacao-card')];
  const status = document.getElementById('ligacoesStatus');
  const q = norm(query);

  if (!q) {
    cards.forEach(c => c.classList.remove('ligacao-match', 'ligacao-dim'));
    if (status) status.textContent = '';
    return 0;
  }

  let encontrados = 0;
  cards.forEach(c => {
    const texto = norm(c.textContent + ' ' + (c.getAttribute('href') || ''));
    const match = texto.includes(q);
    c.classList.toggle('ligacao-match', match);
    c.classList.toggle('ligacao-dim', !match);
    if (match) encontrados++;
  });

  if (status) {
    status.textContent = encontrados
      ? `🔗 ${encontrados} ${L('linksFound')} "${query}"`
      : `${L('linksNone')} "${query}".`;
  }
  return encontrados;
}

// Pesquisa inteligente ("modo IA"): envia a consulta ao Gemini (POST /api/search)
// e mostra uma síntese no painel; se a IA falhar, esconde o painel sem incomodar
async function searchAI(query) {
  const painel = document.getElementById('aiAnswer');
  const corpo  = document.getElementById('aiAnswerBody');
  const label  = document.getElementById('aiAnswerLabel');
  const nota   = document.getElementById('aiAnswerNote');
  if (!painel) return;

  // Mostra o painel com o indicador "a escrever" (três pontos animados)
  label.textContent = L('aiLabel');
  nota.textContent = '';
  corpo.classList.add('ai-loading');
  corpo.innerHTML = '<span></span><span></span><span></span>';
  painel.hidden = false;

  try {
    const r = await fetch(API.search, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ query, lang: currentLang })
    });
    const data = await r.json();
    corpo.classList.remove('ai-loading');
    if (data.ok && data.answer) {
      corpo.textContent = data.answer;
      nota.textContent = L('aiNote');
    } else {
      corpo.textContent = L('aiOffline');
    }
  } catch (_) {
    // IA offline — esconde o painel para não atrapalhar os resultados locais
    corpo.classList.remove('ai-loading');
    painel.hidden = true;
  }
}


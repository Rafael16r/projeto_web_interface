async function hsSearch(query) {
  if (!query) return;
  const aiBox    = document.getElementById('hsAi');
  const aiBody   = document.getElementById('hsAiBody');
  const aiActions= document.getElementById('hsAiActions');
  if (!aiBox || !aiBody) return;

  hsClosePanel();

  // Mostrar loading
  aiBox.hidden = false;
  aiBody.innerHTML = '<div class="hs-ai-loading"><div class="hs-dot"></div><div class="hs-dot"></div><div class="hs-dot"></div></div>';
  aiActions.innerHTML = '';

  // Filtrar cards + ligações em paralelo
  showAllPOI = true;
  fetchPlaces(query);
  filtraLigacoes(query);

  // Chamar API IA
  try {
    const r = await fetch(API.search, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ query, lang: currentLang })
    });
    const data = await r.json();
    if (data.ok && data.answer) {
      aiBody.innerHTML = '';
      hsTypewriter(aiBody, data.answer);
    } else {
      aiBody.textContent = L('aiOffline');
    }
  } catch (_) {
    aiBody.textContent = 'Sem ligação ao servidor IA. Os resultados abaixo foram filtrados localmente.';
  }

  // Chips de ação
  const chips = hsActionChips(query);
  aiActions.innerHTML = chips.map(c =>
    `<button class="hs-ai-action" data-anchor="${c.anchor}">${c.label}</button>`
  ).join('');
  aiActions.querySelectorAll('.hs-ai-action').forEach(btn => {
    btn.addEventListener('click', () => {
      document.getElementById(btn.dataset.anchor)?.scrollIntoView({ behavior:'smooth' });
    });
  });

  // Scroll suave para a secção de resultados
  setTimeout(() => {
    document.getElementById('descobrir')?.scrollIntoView({ behavior:'smooth', block:'start' });
  }, 400);
}


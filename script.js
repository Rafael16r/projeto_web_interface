function _norm(s) {
  return (s||'').toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g,'');
}

function hsRenderPanel(query) {
  const panel = document.getElementById('hsPanel');
  const body  = document.getElementById('hsPanelBody');
  if (!panel || !body) return;

  const q = _norm(query);
  const filtered = q
    ? HS_SUGGESTIONS.filter(it => _norm(it.title+' '+it.sub+' '+it.group+' '+it.q).includes(q))
    : HS_SUGGESTIONS;

  if (!filtered.length) {
    body.innerHTML = `<div class="hs-no-results">Sem sugestões para "<strong>${query}</strong>" — prima Enter para pesquisar com IA</div>`;
    panel.hidden = false; return;
  }

  const groups = {};
  for (const it of filtered) { (groups[it.group] = groups[it.group]||[]).push(it); }

  let html = '';
  const entries = Object.entries(groups);
  entries.forEach(([grp, items], gi) => {
    html += `<div class="hs-group-label">${grp}</div>`;
    items.forEach(it => {
      html += `<div class="hs-item" data-q="${it.q}" data-anchor="${it.anchor}">
        <div class="hs-item-icon">${it.icon}</div>
        <div class="hs-item-text">
          <div class="hs-item-title">${it.title}</div>
          <div class="hs-item-sub">${it.sub}</div>
        </div>
        <span class="hs-item-arr">→</span>
      </div>`;
    });
    if (gi < entries.length - 1) html += '<div class="hs-sep"></div>';
  });
  body.innerHTML = html;

  body.querySelectorAll('.hs-item').forEach(el => {
    el.addEventListener('click', () => {
      const q2 = el.dataset.q;
      document.getElementById('tourismQuery').value = q2;
      panel.hidden = true;
      hsSearch(q2);
    });
  });

  panel.hidden = false;
}

function hsClosePanel() {
  const p = document.getElementById('hsPanel');
  if (p) p.hidden = true;
}

// Typewriter effect
function hsTypewriter(el, text, speed = 18) {
  el.classList.add('hs-ai-cursor');
  let i = 0;
  el.textContent = '';
  const tick = () => {
    if (i < text.length) {
      el.textContent += text[i++];
      setTimeout(tick, speed);
    } else {
      el.classList.remove('hs-ai-cursor');
    }
  };
  tick();
}


// Newsletter — envia a subscrição à API Flask (POST /api/newsletter);
// se a API estiver offline, confirma localmente como fallback
document.querySelector('.newsletter-form')?.addEventListener('submit', async (e) => {
  e.preventDefault();
  const input = e.target.querySelector('input');
  const status = document.getElementById('newsletterStatus');
  const diz = (msg) => { if (status) status.textContent = msg; else alert(msg); };
  if (!input.value || !/^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/.test(input.value.trim())) return;
  try {
    const r = await fetch(API.newsletter, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ email: input.value.trim() })
    });
    const data = await r.json();
    if (r.status === 409 || data.duplicate) { diz(L('newsDuplicate')); return; }
    if (!r.ok) throw new Error('API ' + r.status);
    input.value = '';
    diz(L('newsOk'));
  } catch (_) {
    // API offline — regista localmente para não perder a interação
    input.value = '';
    diz(L('newsOffline'));
  }
});

// Formulário de Contacto – validação com JavaScript puro
const contactoForm = document.getElementById('contactoForm');
if (contactoForm) {
  const rules = {
    'cf-nome':       { el: null, err: null, validate: v => v.trim().length >= 2 ? '' : L('invalidName') },
    'cf-email':      { el: null, err: null, validate: v => /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v.trim()) ? '' : L('invalidEmail') },
    'cf-assunto':    { el: null, err: null, validate: v => v !== '' ? '' : L('invalidSubject') },
    'cf-mensagem':   { el: null, err: null, validate: v => v.trim().length >= 10 ? '' : L('invalidMessage') },
    'cf-privacidade':{ el: null, err: null, validate: (_, el) => el.checked ? '' : L('invalidPrivacy') },
  };

  Object.keys(rules).forEach(id => {
    rules[id].el  = document.getElementById(id);
    rules[id].err = document.getElementById('err-' + id.replace('cf-', ''));
  });

  // Valida um campo, mostra/limpa a mensagem de erro e devolve se é válido
  function validateField(id) {
    const { el, err, validate } = rules[id];
    const msg = validate(el.value, el);
    err.textContent = msg;
    el.classList.toggle('invalid', msg !== '');
    return msg === '';
  }

  // Valida ao sair do campo (blur) e revalida enquanto o utilizador corrige
  Object.keys(rules).forEach(id => {
    rules[id].el.addEventListener('blur', () => validateField(id));
    rules[id].el.addEventListener('input', () => {
      if (rules[id].el.classList.contains('invalid')) validateField(id);
    });
  });

  contactoForm.addEventListener('submit', async e => {
    e.preventDefault();
    const allValid = Object.keys(rules).map(id => validateField(id)).every(Boolean);
    if (!allValid) {
      // Faz scroll até ao primeiro campo inválido
      const firstInvalid = contactoForm.querySelector('.invalid');
      if (firstInvalid) firstInvalid.scrollIntoView({ behavior: 'smooth', block: 'center' });
      return;
    }
    // Envia a mensagem ao backend (POST /api/contact — validação/moderação no servidor);
    // se a API estiver offline, mantém a confirmação local
    const payload = {
      name: rules['cf-nome'].el.value,
      email: rules['cf-email'].el.value,
      subject: rules['cf-assunto'].el.value,
      message: rules['cf-mensagem'].el.value
    };
    let aceite = true;
    try {
      const r = await fetch(API.contact, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(payload)
      });
      const data = await r.json().catch(() => ({}));
      if (!r.ok && data && data.errors) {
        // O backend recusou (validação/moderação) — mostra o motivo no primeiro erro
        aceite = false;
        const err = document.getElementById('err-mensagem');
        if (err) err.textContent = data.reason || L('serverRejected');
      }
    } catch (_) { /* API offline — segue com confirmação local */ }
    if (!aceite) return;
    contactoForm.reset();
    Object.keys(rules).forEach(id => rules[id].el.classList.remove('invalid'));
    const success = document.getElementById('formSuccess');
    success.classList.add('visible');
    success.scrollIntoView({ behavior: 'smooth', block: 'center' });
    setTimeout(() => success.classList.remove('visible'), 6000);
  });
}


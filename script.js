// Evita envios duplicados enquanto uma resposta está a caminho
let chatOcupado = false;

// Envia a pergunta ao backend (com o histórico recente como contexto)
// e mostra a resposta; depois sugere perguntas de seguimento
async function sendChat(textoForcado) {
  const pergunta = (textoForcado || chatInput.value || '').trim();
  if (!pergunta || chatOcupado) return;
  chatOcupado = true;
  const btn = document.getElementById('chatSendBtn');
  if (btn) btn.disabled = true;
  chatInput.value = '';

  const historicoEnviar = chatHistory.slice(-8); // contexto antes desta pergunta
  addChatBubble(pergunta, true);

  // Indicador "a escrever" com três pontos animados (não entra no histórico)
  const espera = addChatBubble('', false, false);
  espera.classList.add('chat-typing');
  espera.setAttribute('aria-label', L('thinking'));
  espera.innerHTML = '<span></span><span></span><span></span>';

  let resposta;
  try {
    const r = await fetch(API.chat, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ message: pergunta, lang: currentLang, history: historicoEnviar })
    });
    const data = await r.json();
    resposta = (data.ok && data.reply) ? limpaMarkdown(data.reply) : localBotReply(pergunta);
  } catch (_) {
    resposta = localBotReply(pergunta);
  } finally {
    espera.classList.remove('chat-typing');
    espera.removeAttribute('aria-label');
    espera.textContent = resposta;
    chatHistory.push({ role: 'bot', text: resposta });
    guardaHistorico();
    chatOcupado = false;
    if (btn) btn.disabled = false;
    chatInput.focus();
    chatMessages.scrollTop = chatMessages.scrollHeight;
    // Sugestões de seguimento relacionadas com o que foi perguntado
    renderSuggestions(escolheFollowups(pergunta));
  }
}


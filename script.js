// Constrói o cartão HTML de um evento (mesma estrutura dos cartões estáticos)
function buildEventCard(ev) {
  const card = document.createElement('div');
  card.className = 'event-card';
  const meta = [];
  if (ev.location) meta.push(`<span>📍 ${ev.location}</span>`);
  if (ev.time) meta.push(`<span>🕐 ${ev.time}</span>`);
  card.innerHTML = `
    <div class="event-date">
      <span class="event-day">${String(ev.day || '').padStart(2, '0')}</span>
      <span class="event-month">${ev.month_label || ''}</span>
    </div>
    <div class="event-info">
      <span class="event-cat">${ev.category || L('eventDefaultCat')}</span>
      <h4>${ev.title}</h4>
      ${ev.summary ? `<p>${ev.summary}</p>` : ''}
      ${meta.length ? `<div class="event-meta">${meta.join('')}</div>` : ''}
    </div>
    <a href="${ev.link}" target="_blank" rel="noopener" class="btn btn-sm">${L('eventDetails')}</a>`;
  return card;
}

// Ordena os eventos por proximidade: os próximos primeiro; uma data que
// já passou este ano conta como do próximo ano (vai para o fim da agenda)
function ordenaEventos(lista) {
  const hoje = new Date();
  const inicioHoje = new Date(hoje.getFullYear(), hoje.getMonth(), hoje.getDate());
  const chave = (ev) => {
    if (!ev.month || !ev.day) return Infinity;
    let d = new Date(hoje.getFullYear(), ev.month - 1, ev.day);
    if (d < inicioHoje) d = new Date(hoje.getFullYear() + 1, ev.month - 1, ev.day);
    return d.getTime();
  };
  return [...lista].sort((a, b) => chave(a) - chave(b));
}

// Desenha os eventos na lista da secção Agenda (próximos primeiro, máximo 6)
function renderEvents(lista) {
  const alvo = document.getElementById('eventsList');
  if (!alvo) return;
  alvo.innerHTML = '';
  ordenaEventos(lista).slice(0, 6).forEach(ev => alvo.appendChild(buildEventCard(ev)));
}

// Vai buscar os eventos e as datas reais à Agenda Cultural de Guimarães
// (em.guimaraes.pt, através do backend); em falha usa a lista local
async function fetchEvents() {
  try {
    const r = await fetch(API.events);
    if (!r.ok) throw new Error('API ' + r.status);
    const data = await r.json();
    if (!data.results || !data.results.length) throw new Error('sem eventos');
    renderEvents(data.results);
  } catch (_) {
    renderEvents(FALLBACK_EVENTS[currentLang]);
  }
  // Se o utilizador já escolheu uma data, destaca os eventos correspondentes
  const dataEscolhida = document.getElementById('tourismDate')?.value;
  if (dataEscolhida) destacaEventosNaData(dataEscolhida);
}

